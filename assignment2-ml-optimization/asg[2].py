# Sparsity enforcement
def apply_sparsity(model, sparsity_ratio=0.6):
    """Enforce sparsity in fc1 and fc2 layers so that only sparsity_ratio of weights are non-zero."""
    with torch.no_grad():
        for name, param in model.named_parameters():
            if 'fc1' in name or 'fc2' in name:
                flat = param.view(-1)
                k = int(len(flat) * sparsity_ratio)
                if k < len(flat):
                    threshold = torch.topk(torch.abs(flat), k, largest=True).values[-1]
                    mask = torch.abs(flat) >= threshold
                    flat *= mask.float()

# Training function
def train_model(model, train_loader, test_loader, criterion, optimizer, n_epochs, special_optimizer=None):
    train_losses, test_losses, test_accuracies = [], [], []
    step = 0

    if special_optimizer == 'IterateAveraging':
        avg_model = copy.deepcopy(model)
        avg_counter = 1

    for epoch in range(n_epochs):
        model.train()
        for i, (images, labels) in enumerate(train_loader):
            images, labels = images.to(device), labels.to(device)

            if isinstance(optimizer, torch.optim.LBFGS):
                def closure():
                    optimizer.zero_grad()
                    output = model(images)
                    loss = criterion(output, labels)
                    loss.backward()
                    return loss
                loss = optimizer.step(closure)
                loss = closure()  # For logging
            else:
                optimizer.zero_grad()
                outputs = model(images)
                loss = criterion(outputs, labels)
                loss.backward()
                optimizer.step()

                if special_optimizer == 'SparseCEL':
                    apply_sparsity(model, sparsity_ratio=0.6)

            step += 1
            train_losses.append((step, loss.item()))

            if special_optimizer == 'IterateAveraging' and (step % 100 == 0):
                for p1, p2 in zip(avg_model.parameters(), model.parameters()):
                    p1.data += p2.data
                    avg_counter += 1

        model.eval()
        test_loss = 0
        correct = 0
        with torch.no_grad():
            for images, labels in test_loader:
                images, labels = images.to(device), labels.to(device)
                outputs = model(images)
                test_loss += criterion(outputs, labels).item()
                pred = outputs.argmax(dim=1)
                correct += pred.eq(labels).sum()

        test_loss /= len(test_loader.dataset)
        test_losses.append((step, test_loss))
        test_accuracy = 100. * correct / len(test_loader.dataset)
        test_accuracies.append((step, test_accuracy.item()))

        print(f"Epoch {epoch}: Loss {test_loss:.4f}, Accuracy {test_accuracy:.2f}%")

    if special_optimizer == 'IterateAveraging':
        for p in avg_model.parameters():
            p.data /= avg_counter
        model.load_state_dict(avg_model.state_dict())

    return train_losses, test_losses, test_accuracies

